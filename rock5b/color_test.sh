#!/bin/bash

# Define color variables
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
NOCOLOR='\033[0m'

echo -e "${RED}This is red text${NOCOLOR}"
echo -e "${GREEN}This is green text${NOCOLOR}"
echo -e "${YELLOW}This is yellow text${NOCOLOR}"
echo -e "${BLUE}This is blue text${NOCOLOR}"
